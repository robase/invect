/**
 * Acme Dashboard — Pre-existing App Schema
 *
 * This represents the schema a startup would already have BEFORE
 * adding Invect. It's a simple SaaS admin backend with customers,
 * products, orders, and team members.
 *
 * When Invect is added, `npx invect-cli generate` merges Invect's
 * core tables + plugin tables INTO the same schema file, so
 * Drizzle Kit manages everything together.
 */

