// =============================================================================
// Append-mode Generators
// =============================================================================
//
// These produce only the table definitions, relations, and type exports
// for appending to an existing Drizzle schema file. They include:
//   - A commented section marker
//   - Additional imports needed (inserted at top when used by CLI)
//   - Table definitions
//   - Relations
//   - Type exports
//
// The CLI handles merging the import lines into the existing file.
// This approach mirrors how better-auth generates schema code that can
// be appended to the user's existing schema file.

export interface AppendSchemaResult {
  /** Import statements that need to be added (CLI merges these) */
  imports: string[];
  /** Table + relation + type code to append */
  code: string;
}

export function generateSqliteSchemaAppend(schema: MergedSchema): AppendSchemaResult {
  const runtimeImports = collectSqliteRuntimeDefaultImports(schema);
  const imports = [
    `import { sqliteTable, text, integer, primaryKey, type AnySQLiteColumn } from 'drizzle-orm/sqlite-core';`,
    `import { relations, sql } from 'drizzle-orm';`,
    `import { randomUUID } from 'crypto';`,
  ];

  for (const [importPath, identifiers] of runtimeImports.entries()) {
    imports.push(`import { ${Array.from(identifiers).sort().join(', ')} } from '${importPath}';`);
  }

  const lines: string[] = [];
  lines.push(``);
  lines.push(`// =============================================================================`);
  lines.push(`// Invect tables — AUTO-GENERATED by @invect/cli`);
  lines.push(`// Do not edit below this line. Run \`npx invect-cli generate\` to regenerate.`);
  lines.push(`// =============================================================================`);
  lines.push(``);

  for (const table of schema.tables) {
    lines.push(generateSqliteTable(table, schema));
    lines.push(``);
  }

  lines.push(generateRelations(schema));
  lines.push(``);

  for (const table of schema.tables) {
    const typeName = capitalize(table.name);
    lines.push(`export type ${typeName} = typeof ${table.name}.$inferSelect;`);
    lines.push(`export type New${typeName} = typeof ${table.name}.$inferInsert;`);
  }

  return { imports, code: lines.join('\n') };
}

