function generateRawSqlTable(
  table: MergedTable,
  dialect: 'sqlite' | 'postgresql' | 'mysql',
): string {
  const lines: string[] = [];
  const { definition } = table;
  const dbTableName = definition.tableName || toSnakeCase(table.name);
  const hasCompositePK = !!definition.compositePrimaryKey?.length;

  lines.push(`CREATE TABLE IF NOT EXISTS ${quoteIdent(dbTableName, dialect)} (`);

  const colDefs: string[] = [];
  const constraints: string[] = [];

  for (const [fieldName, field] of Object.entries(definition.fields)) {
    const colName = toSnakeCase(fieldName);
    const colType = rawSqlColumnType(field, dialect, dbTableName, colName);
    let colDef = `  ${quoteIdent(colName, dialect)} ${colType}`;

    if (field.primaryKey && !hasCompositePK) {
      colDef += ' PRIMARY KEY';
    }
    if (field.required !== false && !field.primaryKey) {
      colDef += ' NOT NULL';
    }
    if (field.unique) {
      colDef += ' UNIQUE';
    }
    const def = rawSqlDefault(field, dialect);
    if (def) {
      colDef += ` DEFAULT ${def}`;
    }
    if (field.references) {
      const refTableName = field.references.table;
      const refField = toSnakeCase(field.references.field);
      const onDelete = (field.references.onDelete || 'NO ACTION').toUpperCase().replace('_', ' ');
      constraints.push(
        `  FOREIGN KEY (${quoteIdent(colName, dialect)}) REFERENCES ${quoteIdent(refTableName, dialect)}(${quoteIdent(refField, dialect)}) ON DELETE ${onDelete}`,
      );
    }
    colDefs.push(colDef);
  }

  if (hasCompositePK) {
    const pkCols = (definition.compositePrimaryKey ?? [])
      .map((f) => quoteIdent(toSnakeCase(f), dialect))
      .join(', ');
    constraints.push(`  PRIMARY KEY (${pkCols})`);
  }

  const allParts = [...colDefs, ...constraints];
  lines.push(allParts.join(',\n'));
  lines.push(`);`);

  return lines.join('\n');
}

function quoteIdent(name: string, dialect: 'sqlite' | 'postgresql' | 'mysql'): string {
  if (dialect === 'mysql') {
    return `\`${name}\``;
  }
  return `"${name}"`;
}

function rawSqlColumnType(
  field: PluginFieldAttribute,
  dialect: 'sqlite' | 'postgresql' | 'mysql',
  tableName: string,
  colName: string,
): string {
  if (isEnumType(field.type)) {
    if (dialect === 'sqlite') {
      return 'TEXT';
    }
    if (dialect === 'postgresql') {
      return `${tableName}_${colName}`;
    }
    // MySQL inline enum
    return `ENUM(${field.type.map((v) => `'${v}'`).join(', ')})`;
  }

  const typeMap: Record<string, Record<string, string>> = {
    sqlite: {
      string: 'TEXT',
      text: 'TEXT',
      uuid: 'TEXT',
      number: 'INTEGER',
      bigint: 'INTEGER',
      boolean: 'INTEGER',
      date: 'TEXT',
      json: 'TEXT',
    },
    postgresql: {
      string: 'TEXT',
      text: 'TEXT',
      uuid: 'UUID',
      number: 'INTEGER',
      bigint: 'BIGINT',
      boolean: 'BOOLEAN',
      date: 'TIMESTAMP WITH TIME ZONE',
      json: 'JSONB',
    },
    mysql: {
      string: `VARCHAR(${field.maxLength || 255})`,
      text: 'TEXT',
      uuid: 'VARCHAR(36)',
      number: 'INT',
      bigint: 'BIGINT',
      boolean: 'BOOLEAN',
      date: 'TIMESTAMP',
      json: 'JSON',
    },
  };

  const t = typeof field.type === 'string' ? field.type : 'text';
  return typeMap[dialect][t] || 'TEXT';
}

function rawSqlDefault(
  field: PluginFieldAttribute,
  dialect: 'sqlite' | 'postgresql' | 'mysql',
): string | null {
  if (field.defaultValue === undefined) {
    return null;
  }
  if (field.defaultValue === 'uuid()') {
    if (dialect === 'postgresql') {
      return `gen_random_uuid()`;
    }
    // SQLite/MySQL: no built-in UUID default; omit (app generates)
    return null;
  }
  if (field.defaultValue === 'now()') {
    if (dialect === 'sqlite') {
      return `CURRENT_TIMESTAMP`;
    }
    if (dialect === 'postgresql') {
      return `NOW()`;
    }
    return `CURRENT_TIMESTAMP`;
  }
  if (typeof field.defaultValue === 'boolean') {
    if (dialect === 'sqlite') {
      return field.defaultValue ? '1' : '0';
    }
    return field.defaultValue ? 'TRUE' : 'FALSE';
  }
  if (typeof field.defaultValue === 'number') {
    return String(field.defaultValue);
  }
  if (typeof field.defaultValue === 'string') {
    return `'${field.defaultValue}'`;
  }
  return null;
}
