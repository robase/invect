interface EnumDef {
  varName: string;
  dbName: string;
  values: string[];
  tableName: string;
  fieldName: string;
}

function collectEnums(schema: MergedSchema): EnumDef[] {
  const enums: EnumDef[] = [];

  for (const table of schema.tables) {
    for (const [fieldName, field] of Object.entries(table.definition.fields)) {
      if (isEnumType(field.type)) {
        const varName = enumVarName(table.name, fieldName);
        const dbName = `${toSnakeCase(table.name)}_${toSnakeCase(fieldName)}`;
        enums.push({
          varName,
          dbName,
          values: field.type,
          tableName: table.name,
          fieldName,
        });
      }
    }
  }

  return enums;
}

