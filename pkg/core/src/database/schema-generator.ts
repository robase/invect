/**
 * Drizzle Schema Generators
 *
 * Converts the merged abstract schema (core + plugins) into three
 * dialect-specific Drizzle ORM schema files as TypeScript source code.
 *
 * Each generator produces a complete, self-contained .ts file that can
 * be used by Drizzle Kit for migration generation.
 */

import type { PluginFieldAttribute, PluginFieldType } from 'src/types/plugin.types';
import type { MergedSchema, MergedTable } from './schema-merger';

// =============================================================================
// Common Utilities
// =============================================================================

/**
 * Convert camelCase to snake_case for DB column names.
 */
function toSnakeCase(str: string): string {
  return str.replace(/[A-Z]/g, (letter) => `_${letter.toLowerCase()}`);
}

/**
 * Check if a field type is an enum (string array).
 */
function isEnumType(type: PluginFieldType): type is string[] {
  return Array.isArray(type);
}

/**
 * Generate a variable name for an enum from a table + field.
 * e.g., flowRuns + status → flowRunStatusEnum
 */
function enumVarName(tableName: string, fieldName: string): string {
  return `${tableName}${fieldName.charAt(0).toUpperCase() + fieldName.slice(1)}Enum`;
}

const SQLITE_RUNTIME_DEFAULT_IMPORTS: Record<string, string> = {
  FlowRunStatus: '@invect/core',
  NodeExecutionStatus: '@invect/core',
};

function collectSqliteRuntimeDefaultImports(schema: MergedSchema): Map<string, Set<string>> {
  const importsByModule = new Map<string, Set<string>>();

  for (const table of schema.tables) {
    for (const field of Object.values(table.definition.fields)) {
      if (typeof field.defaultValue !== 'string' || !field.typeAnnotation) {
        continue;
      }

      const importPath = SQLITE_RUNTIME_DEFAULT_IMPORTS[field.typeAnnotation];
      if (!importPath) {
        continue;
      }

      const existing = importsByModule.get(importPath) ?? new Set<string>();
      existing.add(field.typeAnnotation);
      importsByModule.set(importPath, existing);
    }
  }

  return importsByModule;
}

// =============================================================================
// SQLite Generator
// =============================================================================

export function generateSqliteSchema(schema: MergedSchema): string {
  const lines: string[] = [];
  const runtimeImports = collectSqliteRuntimeDefaultImports(schema);

  // Header
  lines.push(`// SQLite schema for Invect — AUTO-GENERATED by @invect/cli`);
  lines.push(`// Do not edit manually. Run \`npx invect-cli generate\` to regenerate.`);
  lines.push(``);
  lines.push(
    `import { sqliteTable, text, integer, primaryKey, type AnySQLiteColumn } from 'drizzle-orm/sqlite-core';`,
  );
  lines.push(`import { relations, sql } from 'drizzle-orm';`);
  for (const [importPath, identifiers] of runtimeImports.entries()) {
    lines.push(`import { ${Array.from(identifiers).sort().join(', ')} } from '${importPath}';`);
  }
  lines.push(`import { randomUUID } from 'crypto';`);
  lines.push(``);
  lines.push(`// Type imports — these are used for $type<>() annotations`);
  lines.push(`import type { JSONValue } from '.';`);
  lines.push(``);

  // Collect all typeAnnotations to generate type imports
  const typeAnnotations = collectTypeAnnotations(schema);
  if (typeAnnotations.size > 0) {
    lines.push(`// Plugin & core type annotations`);
    lines.push(`// You may need to adjust these imports based on your project structure`);
    lines.push(``);
  }

  // Tables
  lines.push(`// =============================================================================`);
  lines.push(`// Tables`);
  lines.push(`// =============================================================================`);
  lines.push(``);

  for (const table of schema.tables) {
    lines.push(generateSqliteTable(table, schema));
    lines.push(``);
  }

  // Relations
  lines.push(`// =============================================================================`);
  lines.push(`// Relations`);
  lines.push(`// =============================================================================`);
  lines.push(``);
  lines.push(generateRelations(schema));

  // Type exports
  lines.push(``);
  lines.push(`// =============================================================================`);
  lines.push(`// Type exports`);
  lines.push(`// =============================================================================`);
  lines.push(``);
  for (const table of schema.tables) {
    const varName = table.name;
    const typeName = capitalize(table.name);
    lines.push(`export type ${typeName} = typeof ${varName}.$inferSelect;`);
    lines.push(`export type New${typeName} = typeof ${varName}.$inferInsert;`);
  }

  return lines.join('\n');
}

function generateSqliteTable(table: MergedTable, schema: MergedSchema): string {
  const lines: string[] = [];
  const { name, definition } = table;
  const dbTableName = definition.tableName || toSnakeCase(name);
  const hasCompositePK = !!definition.compositePrimaryKey?.length;

  lines.push(`export const ${name} = sqliteTable('${dbTableName}', {`);

  for (const [fieldName, field] of Object.entries(definition.fields)) {
    const colName = toSnakeCase(fieldName);
    lines.push(`  ${fieldName}: ${sqliteColumn(fieldName, colName, field, name, schema)},`);
  }

  if (hasCompositePK) {
    const pkCols = (definition.compositePrimaryKey ?? []).map((f) => `table.${f}`).join(', ');
    lines.push(`}, (table) => [primaryKey({ columns: [${pkCols}] })]);`);
  } else {
    lines.push(`});`);
  }

  return lines.join('\n');
}

function sqliteColumn(
  fieldName: string,
  colName: string,
  field: PluginFieldAttribute,
  tableName: string,
  schema?: MergedSchema,
): string {
  let col: string;

  if (isEnumType(field.type)) {
    // SQLite: enums are just text columns
    col = `text('${colName}')`;
  } else {
    switch (field.type) {
      case 'string':
      case 'text':
        col = `text('${colName}')`;
        break;
      case 'uuid':
        col = `text('${colName}')`;
        break;
      case 'number':
      case 'bigint':
        col = `integer('${colName}')`;
        break;
      case 'boolean':
        col = `integer('${colName}', { mode: 'boolean' })`;
        break;
      case 'date':
        col = `text('${colName}')`;
        break;
      case 'json':
        col = `text('${colName}', { mode: 'json' })`;
        break;
      default:
        col = `text('${colName}')`;
    }
  }

  // Type annotation
  if (field.typeAnnotation) {
    col += `.$type<${field.typeAnnotation}>()`;
  }

  // Primary key
  if (field.primaryKey) {
    col += `.primaryKey()`;
  }

  // Not null
  if (field.required !== false) {
    col += `.notNull()`;
  }

  // Unique
  if (field.unique) {
    col += `.unique()`;
  }

  // Default value
  if (field.defaultValue !== undefined) {
    col += sqliteDefault(field.defaultValue, field.type, field.typeAnnotation);
  }

  // Foreign key
  if (field.references) {
    const refTable = findTableVarForDbName(field.references.table, schema);
    const refField = field.references.field;
    const onDelete = field.references.onDelete || 'no action';
    if (refTable === tableName) {
      col += `.references((): AnySQLiteColumn => ${refTable}.${refField}, { onDelete: '${onDelete}' })`;
    } else {
      col += `.references(() => ${refTable}.${refField}, { onDelete: '${onDelete}' })`;
    }
  }

  return col;
}

function sqliteDefault(
  value: string | number | boolean | 'uuid()' | 'now()',
  _type: PluginFieldType,
  typeAnnotation?: string,
): string {
  if (value === 'uuid()') {
    return `.$defaultFn(() => randomUUID())`;
  }
  if (value === 'now()') {
    return `.default(sql\`CURRENT_TIMESTAMP\`)`;
  }
  if (typeof value === 'boolean') {
    return `.default(${value})`;
  }
  if (typeof value === 'number') {
    return `.default(${value})`;
  }
  if (typeof value === 'string') {
    if (typeAnnotation && SQLITE_RUNTIME_DEFAULT_IMPORTS[typeAnnotation]) {
      return `.default(${typeAnnotation}.${value})`;
    }
    return `.default('${value}')`;
  }
  return '';
}

