/**
 * Build a map from DB table name → JS variable name from the schema.
 */
function buildTableMap(schema: MergedSchema): Map<string, string> {
  const map = new Map<string, string>();
  for (const table of schema.tables) {
    const dbName = table.definition.tableName || toSnakeCase(table.name);
    map.set(dbName, table.name);
    // Also map by logical name for convenience
    map.set(table.name, table.name);
  }
  return map;
}

/**
 * Find the JS variable name for a given DB table name.
 * Uses the schema to do a proper lookup (e.g., 'execution_traces' → 'nodeExecutions').
 * Falls back to camelCase heuristic if no schema provided.
 */
function findTableVarForDbName(dbName: string, schema?: MergedSchema): string {
  if (schema) {
    const tableMap = buildTableMap(schema);
    const found = tableMap.get(dbName);
    if (found) {
      return found;
    }
  }
  // Fallback: convert snake_case to camelCase
  if (dbName.includes('_')) {
    return dbName.replace(/_([a-z])/g, (_, c) => c.toUpperCase());
  }
  return dbName;
}

function capitalize(str: string): string {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

