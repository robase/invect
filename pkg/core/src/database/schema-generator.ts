function mysqlColumn(
  fieldName: string,
  colName: string,
  field: PluginFieldAttribute,
  tableName: string,
  schema?: MergedSchema,
): string {
  const maxLen = field.maxLength ?? 255;
  let col: string;

  if (isEnumType(field.type)) {
    const enumValues = field.type.map((v) => `'${v}'`).join(', ');
    col = `mysqlEnum('${colName}', [${enumValues}])`;
  } else {
    switch (field.type) {
      case 'string':
        col = `varchar('${colName}', { length: ${maxLen} })`;
        break;
      case 'text':
        col = `text('${colName}')`;
        break;
      case 'uuid':
        col = `varchar('${colName}', { length: 36 })`;
        break;
      case 'number':
        col = `int('${colName}')`;
        break;
      case 'bigint':
        col = `bigint('${colName}', { mode: 'bigint' })`;
        break;
      case 'boolean':
        col = `boolean('${colName}')`;
        break;
      case 'date':
        col = `timestamp('${colName}')`;
        break;
      case 'json':
        col = `json('${colName}')`;
        break;
      default:
        col = `varchar('${colName}', { length: ${maxLen} })`;
    }
  }

  // Type annotation
  if (field.typeAnnotation) {
    col += `.$type<${field.typeAnnotation}>()`;
  }

  // Primary key
  if (field.primaryKey) {
    col += `.primaryKey()`;
  }

  // Not null
  if (field.required !== false) {
    col += `.notNull()`;
  }

  // Unique
  if (field.unique) {
    col += `.unique()`;
  }

  // Default value
  if (field.defaultValue !== undefined) {
    col += mysqlDefault(field.defaultValue, field.type);
  }

  // Foreign key
  if (field.references) {
    const refTable = findTableVarForDbName(field.references.table, schema);
    const refField = field.references.field;
    const onDelete = field.references.onDelete || 'no action';
    if (refTable === tableName) {
      col += `.references((): AnyMySqlColumn => ${refTable}.${refField}, { onDelete: '${onDelete}' })`;
    } else {
      col += `.references(() => ${refTable}.${refField}, { onDelete: '${onDelete}' })`;
    }
  }

  return col;
}

