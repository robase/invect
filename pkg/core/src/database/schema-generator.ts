function postgresColumn(
  fieldName: string,
  colName: string,
  field: PluginFieldAttribute,
  tableName: string,
  enums: EnumDef[],
  schema?: MergedSchema,
): string {
  let col: string;

  if (isEnumType(field.type)) {
    const eVar = enums.find((e) => e.tableName === tableName && e.fieldName === fieldName)?.varName;
    col = eVar ? `${eVar}('${colName}')` : `text('${colName}')`;
  } else {
    switch (field.type) {
      case 'string':
      case 'text':
        col = `text('${colName}')`;
        break;
      case 'uuid':
        col = `uuid('${colName}')`;
        break;
      case 'number':
        col = `integer('${colName}')`;
        break;
      case 'bigint':
        col = `bigint('${colName}', { mode: 'bigint' })`;
        break;
      case 'boolean':
        col = `boolean('${colName}')`;
        break;
      case 'date':
        col = `timestamp('${colName}')`;
        break;
      case 'json':
        col = `json('${colName}')`;
        break;
      default:
        col = `text('${colName}')`;
    }
  }

  // Type annotation
  if (field.typeAnnotation) {
    col += `.$type<${field.typeAnnotation}>()`;
  }

  // Primary key
  if (field.primaryKey) {
    col += `.primaryKey()`;
  }

  // Not null
  if (field.required !== false) {
    col += `.notNull()`;
  }

  // Unique
  if (field.unique) {
    col += `.unique()`;
  }

  // Default value
  if (field.defaultValue !== undefined) {
    col += postgresDefault(field.defaultValue, field.type);
  }

  // Foreign key
  if (field.references) {
    const refTable = findTableVarForDbName(field.references.table, schema);
    const refField = field.references.field;
    const onDelete = field.references.onDelete || 'no action';
    if (refTable === tableName) {
      col += `.references((): AnyPgColumn => ${refTable}.${refField}, { onDelete: '${onDelete}' })`;
    } else {
      col += `.references(() => ${refTable}.${refField}, { onDelete: '${onDelete}' })`;
    }
  }

  return col;
}

function postgresDefault(
  value: string | number | boolean | 'uuid()' | 'now()',
  _type: PluginFieldType,
): string {
  if (value === 'uuid()') {
    return `.$default(() => randomUUID())`;
  }
  if (value === 'now()') {
    return `.defaultNow()`;
  }
  if (typeof value === 'boolean') {
    return `.default(${value})`;
  }
  if (typeof value === 'number') {
    return `.default(${value})`;
  }
  if (typeof value === 'string') {
    return `.default('${value}')`;
  }
  return '';
}

