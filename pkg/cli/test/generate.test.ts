/**
 * Drizzle Schema Generation Tests
 *
 * Tests the CLI's schema generators using realistic example schemas.
 * Mirrors the user-auth plugin's generate.test.ts approach:
 *   - Generate for each dialect (sqlite, postgresql, mysql)
 *   - Verify output contains expected table/column definitions
 *   - Verify dialect-specific code (pgEnum, mysqlEnum, sqliteTable, etc.)
 *   - Test with plugins that add tables, extend tables, and conflict
 */

import { describe, it, expect } from 'vitest';
import {
  mergeSchemas,
  generateSqliteSchema,
  generatePostgresSchema,
  generateMysqlSchema,
  CORE_TABLE_NAMES,
} from '@invect/core';

import {
  multiTenantPlugin,
  auditLogPlugin,
  ecommercePlugin,
  minimalPlugin,
  coreExtensionPlugin,
  analyticsPlugin,
  conflictingPlugin,
  disabledMigrationPlugin,
  pluginA,
  pluginB,
  conflictPluginA,
  conflictPluginB,
  authenticationPlugin,
} from './fixtures/example-schemas';

// =============================================================================
// Helper: generate all 3 dialect schemas from a plugin set
// =============================================================================

function generateAll(plugins: any[]) {
  const merged = mergeSchemas(plugins);
  return {
    merged,
    sqlite: generateSqliteSchema(merged),
    postgres: generatePostgresSchema(merged),
    mysql: generateMysqlSchema(merged),
  };
}

// =============================================================================
// 1. Core-only schema (no plugins)
// =============================================================================

describe('generate core-only schema (no plugins)', () => {
  const { sqlite, postgres, mysql, merged } = generateAll([]);

  it('should include all core tables', () => {
    for (const tableName of CORE_TABLE_NAMES) {
      const table = merged.tables.find((t) => t.name === tableName);
      expect(table, `missing table: ${tableName}`).toBeDefined();
      expect(table!.source).toBe('core');
    }
  });

  it('should generate valid SQLite schema', () => {
    expect(sqlite).toContain("from 'drizzle-orm/sqlite-core'");
    expect(sqlite).toContain(
      "import { BatchStatus, FlowRunStatus, NodeExecutionStatus } from '@invect/core';",
    );
    expect(sqlite).toContain("sqliteTable('flows'");
    expect(sqlite).toContain("sqliteTable('flow_versions'");
    expect(sqlite).toContain("sqliteTable('flow_executions'");
    expect(sqlite).toContain("sqliteTable('credentials'");
    expect(sqlite).toContain('default(FlowRunStatus.PENDING)');
    expect(sqlite).toContain('default(NodeExecutionStatus.PENDING)');
    // SQLite uses text for dates
    expect(sqlite).toContain("text('created_at')");
    // SQLite uses integer for booleans
    expect(sqlite).toContain("integer('is_active', { mode: 'boolean' })");
    // SQLite uses text({mode:'json'}) for JSON
    expect(sqlite).toContain("text('inputs', { mode: 'json' })");
  });

  it('should generate valid PostgreSQL schema', () => {
    expect(postgres).toContain("from 'drizzle-orm/pg-core'");
    expect(postgres).toContain("pgTable('flows'");
    expect(postgres).toContain("pgTable('flow_executions'");
    expect(postgres).toContain("pgTable('credentials'");
    // PG uses timestamp for dates
    expect(postgres).toContain("timestamp('created_at')");
    // PG uses boolean natively
    expect(postgres).toContain("boolean('is_active')");
    // PG uses json for JSON
    expect(postgres).toContain("json('inputs')");
    // PG has enum types
    expect(postgres).toContain('pgEnum(');
  });

  it('should generate valid MySQL schema', () => {
    expect(mysql).toContain("from 'drizzle-orm/mysql-core'");
    expect(mysql).toContain("mysqlTable('flows'");
    expect(mysql).toContain("mysqlTable('flow_executions'");
    expect(mysql).toContain("mysqlTable('credentials'");
    // MySQL uses timestamp for dates
    expect(mysql).toContain("timestamp('created_at')");
    // MySQL uses boolean
    expect(mysql).toContain("boolean('is_active')");
    // MySQL has enum types
    expect(mysql).toContain('mysqlEnum(');
  });

  it('should generate type exports for all tables', () => {
    for (const dialect of [sqlite, postgres, mysql]) {
      expect(dialect).toContain('export type Flows =');
      expect(dialect).toContain('export type NewFlows =');
      expect(dialect).toContain('export type Credentials =');
      expect(dialect).toContain('export type NewCredentials =');
    }
  });

  it('should include the AUTO-GENERATED header', () => {
    expect(sqlite).toContain('AUTO-GENERATED by @invect/cli');
    expect(postgres).toContain('AUTO-GENERATED by @invect/cli');
    expect(mysql).toContain('AUTO-GENERATED by @invect/cli');
  });

  it('should generate relations', () => {
    for (const dialect of [sqlite, postgres, mysql]) {
      expect(dialect).toContain('relations(');
    }
  });
});

