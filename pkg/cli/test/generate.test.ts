describe('generate with e-commerce plugin', () => {
  const { sqlite, postgres, mysql, merged } = generateAll([ecommercePlugin]);

  it('should add products, customers, orders, orderItems tables', () => {
    expect(merged.tables.find((t) => t.name === 'products')).toBeDefined();
    expect(merged.tables.find((t) => t.name === 'customers')).toBeDefined();
    expect(merged.tables.find((t) => t.name === 'orders')).toBeDefined();
    expect(merged.tables.find((t) => t.name === 'orderItems')).toBeDefined();
  });

  it('should preserve foreign key relationships', () => {
    const ordersTable = merged.tables.find((t) => t.name === 'orders');
    expect(ordersTable!.definition.fields.customerId.references).toEqual({
      table: 'customers',
      field: 'id',
      onDelete: 'restrict',
    });

    const itemsTable = merged.tables.find((t) => t.name === 'orderItems');
    expect(itemsTable!.definition.fields.orderId.references).toEqual({
      table: 'orders',
      field: 'id',
      onDelete: 'cascade',
    });
  });

  it('SQLite: should generate order status as text (no enum)', () => {
    // SQLite doesn't have real enums, so they become text columns
    expect(sqlite).toContain("sqliteTable('orders'");
    expect(sqlite).toContain("text('status')");
  });

  it('PostgreSQL: should generate order status as pgEnum', () => {
    expect(postgres).toContain("pgTable('orders'");
    expect(postgres).toContain("'pending'");
    expect(postgres).toContain("'shipped'");
    expect(postgres).toContain("'refunded'");
  });

  it('MySQL: should generate order status as mysqlEnum', () => {
    expect(mysql).toContain("mysqlTable('orders'");
    expect(mysql).toContain("'pending'");
    expect(mysql).toContain("'delivered'");
  });

  it('should generate unique constraint on product SKU', () => {
    for (const dialect of [sqlite, postgres, mysql]) {
      expect(dialect).toContain('sku');
      expect(dialect).toContain('.unique()');
    }
  });

  it('should handle default values correctly', () => {
    // defaultValue: 0 for stockQuantity
    for (const dialect of [sqlite, postgres, mysql]) {
      expect(dialect).toContain('.default(0)');
    }
  });
});

